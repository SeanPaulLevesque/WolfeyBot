_FLAG_SETS = {
    "contact": _CONTACT_MOVES,
    "slicing": _SLICING_MOVES,
    "punch": _PUNCH_MOVES,
    "bite": _BITE_MOVES,
    "pulse": _PULSE_MOVES,
    "sound": _SOUND_MOVES,
    "recoil": _RECOIL_MOVES,
}


def move_flags(name: str) -> frozenset[str]:
    """Return the set of property flags for *name* (possibly empty)."""
    return frozenset(flag for flag, s in _FLAG_SETS.items() if name in s)


def move_has_flag(name: str, flag: str) -> bool:
    """True if move *name* carries *flag*."""
    return name in _FLAG_SETS.get(flag, frozenset())


def is_contact(name: str) -> bool:
    """True if move *name* makes contact."""
    return name in _CONTACT_MOVES
